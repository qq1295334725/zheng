# AJAX全称是Asynchronous JavaScript and XML,即异步JavaScript和XML，ajax并不是一门编程语言，而是利用了JavaScript在保证页面不被刷新，页面链接不改变的情况下与服务器进行交互数据并更新部分网页的技术。
# 对于传统的页面而言，如果想更新内容，那么必须刷新整个网页，但是有了ajax，便可以在页面不被刷新的情况下更新内容，在这个过程中，页面实际上是在后头与服务器进行了数据的交互，获取到数据之后，再利用JavaScript改变网页，这样便可以更新网页。

