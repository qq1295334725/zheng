# 当用户在浏览器的地址栏当中输入一个URL并且按下回车的时候，浏览器就会向HTTP服务器发送HTTP请求，并且获取到该URL对应的html文件，服务器会把请求到的响应对象返回给浏览器。
# 浏览器分析返回的响应对象中的HTML，一旦发现其中引用了很多的其他文件。浏览器便会再次发送请求去获取图片，CSS文件或者JS文件等。
# 当所有的文件都下载成功之后，浏览器会根据HTML语法结构将网页完整的显示出来。


