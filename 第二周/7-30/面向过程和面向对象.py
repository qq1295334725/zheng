# 第一种:
# 第一步：查找资料，比对几款手机的配置和价格，来最终决定买那一款
# 第二步：根据自己的小金库还有自己对手机的需求，决定买个iPhonex
# 第三步：拿着钱去实体店买手机，业务员听说要买iPhonex，她给你推荐了一款手机iPhonexx，他一忽悠，说买iPhonexx比iPhonex更便宜，配置也更高。于是一想反正都是iPhone，最终买了iPhonexx。
